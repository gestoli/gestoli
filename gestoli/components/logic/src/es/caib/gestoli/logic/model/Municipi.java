package es.caib.gestoli.logic.model;

import java.io.Serializable;

/**
 * Municipis
 * 
 * @author Miquel Angel Amengual <miquelaa@limit.es>
 */
public class Municipi implements Serializable {

	private static final long serialVersionUID = 1L;

	private Long id;
	private String nom;
	private Long illa;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	public Long getIlla() {
		return illa;
	}
	public void setIlla(Long illa) {
		this.illa = illa;
	}
}
