package es.caib.gestoli.logic.model;

import java.io.Serializable;
import java.util.Date;

/**
 * Model de l'objecte de Sortida d'orujo.
 * 
 * @author Miquel Angel Amengual <miquelaa@limit.es>
 */
public class SortidaOrujo implements Serializable {

	private static final long serialVersionUID = 1L;

	private Long id;
	private Date data;
	private Double kilos;
	private String albara;
	private String desti;
	private String document;
	private Boolean valid;
	private Establiment establiment;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Date getData() {
		return data;
	}
	public void setData(Date data) {
		this.data = data;
	}
	public Double getKilos() {
		return kilos;
	}
	public void setKilos(Double kilos) {
		this.kilos = kilos;
	}
	public String getAlbara() {
		return albara;
	}
	public void setAlbara(String albara) {
		this.albara = albara;
	}
	public String getDesti() {
		return desti;
	}
	public void setDesti(String desti) {
		this.desti = desti;
	}
	public String getDocument() {
		return document;
	}
	public void setDocument(String document) {
		this.document = document;
	}
	public Boolean getValid() {
		return valid;
	}
	public void setValid(Boolean valid) {
		this.valid = valid;
	}
	public Establiment getEstabliment() {
		return establiment;
	}
	public void setEstabliment(Establiment establiment) {
		this.establiment = establiment;
	}
}
