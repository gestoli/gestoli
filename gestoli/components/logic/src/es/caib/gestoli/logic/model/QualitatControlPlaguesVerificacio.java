package es.caib.gestoli.logic.model;

import java.util.Date;

public class QualitatControlPlaguesVerificacio extends QualitatControl {

	private static final long serialVersionUID = 1L;
	private QualitatControlPlagues controlPlaga;
	
	public QualitatControlPlaguesVerificacio(){
		super();
	}
	
	
	public QualitatControlPlaguesVerificacio(Long id, String plagaObjectiu,
			Date dataRealitzacio, QualitatDescripcioPersonal responsableVerificacio,
			Boolean satisfactori, QualitatControlPlagues controlPlaga){
		this.id = id;
		this.objectiu = plagaObjectiu;
		this.dataVerificacio = dataRealitzacio;
		this.responsableVerificacio = responsableVerificacio;
		this.satisfactori = satisfactori;
		this.controlPlaga = controlPlaga;
	}

	public QualitatControlPlagues getControlPlaga() {
		return controlPlaga;
	}
	public void setControlPlaga(QualitatControlPlagues controlPlaga) {
		this.controlPlaga = controlPlaga;
	}
	
	
	
}
