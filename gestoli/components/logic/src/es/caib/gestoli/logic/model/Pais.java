package es.caib.gestoli.logic.model;

import java.io.Serializable;
import java.util.HashSet;
import java.util.Set;

public class Pais implements Serializable {

	private static final long serialVersionUID = 1L;

	private Long id;
	private String nomcas;
	private String nomcat;
	private Boolean ue;
	private String iso;
	private Set provincies = new HashSet(0);
	public static String ESPANYA_ISO = "ESP";
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getNomcas() {
		return nomcas;
	}
	public void setNomcas(String nomcas) {
		this.nomcas = nomcas;
	}
	public String getNomcat() {
		return nomcat;
	}
	public void setNomcat(String nomcat) {
		this.nomcat = nomcat;
	}
	public Boolean getUe() {
		return ue;
	}
	public void setUe(Boolean ue) {
		this.ue = ue;
	}
	public String getIso() {
		return iso;
	}
	public void setIso(String iso) {
		this.iso = iso;
	}
	public Set getProvincies() {
		return provincies;
	}
	public void setProvincies(Set provincies) {
		this.provincies = provincies;
	}
}
