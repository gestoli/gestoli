package es.caib.gestoli.logic.model;

import java.io.Serializable;

/**
 * Javadoc de les clases Model de Gest Oli
 * @author Miquel Angel Amengual <miquelaa@limit.es>
 */
public class RendimentVarietat implements Serializable {

	private static final long serialVersionUID = 1L;

	private Long id;
	private Campanya campanya;
	private VarietatOliva varietatOliva;
	private Long idVarietatOliva;
	private String tipusRendiment;
	private Double rendiment;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Campanya getCampanya() {
		return campanya;
	}
	public void setCampanya(Campanya campanya) {
		this.campanya = campanya;
	}
	public VarietatOliva getVarietatOliva() {
		return varietatOliva;
	}
	public void setVarietatOliva(VarietatOliva varietatOliva) {
		this.varietatOliva = varietatOliva;
	}
	public String getTipusRendiment() {
		return tipusRendiment;
	}
	public void setTipusRendiment(String tipusRendiment) {
		this.tipusRendiment = tipusRendiment;
	}
	public Double getRendiment() {
		return rendiment;
	}
	public void setRendiment(Double rendiment) {
		this.rendiment = rendiment;
	}
	public Long getIdVarietatOliva() {
		return idVarietatOliva;
	}
	public void setIdVarietatOliva(Long idVarietatOliva) {
		this.idVarietatOliva = idVarietatOliva;
	}
}
