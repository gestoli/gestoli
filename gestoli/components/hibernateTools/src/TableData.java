/**
 * Contiene la informacion de una tabla
 */


/**
 * @author agarcia
 *
 */
public class TableData {

	private String tableName;
    private String classname;
    private String plural;
    private String singular;
    private String acronimo;

	public TableData(String tableName, String classname, String plural,
			String singular, String acronimo) {
		super();
		this.tableName = tableName;
		this.classname = classname;
		this.plural = plural;
		this.singular = singular;
		this.acronimo = acronimo;
	}
	/**
	 * @return the tableName
	 */
	public String getTableName() {
		return this.tableName;
	}
	/**
	 * @param tableName the tableName to set
	 */
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	/**
	 * @return the classname
	 */
	public String getClassname() {
		return this.classname;
	}
	/**
	 * @param classname the classname to set
	 */
	public void setClassname(String classname) {
		this.classname = classname;
	}
	/**
	 * @return the plural
	 */
	public String getPlural() {
		return this.plural;
	}
	/**
	 * @param plural the plural to set
	 */
	public void setPlural(String plural) {
		this.plural = plural;
	}
	/**
	 * @return the singular
	 */
	public String getSingular() {
		return this.singular;
	}
	/**
	 * @param singular the singular to set
	 */
	public void setSingular(String singular) {
		this.singular = singular;
	}
	/**
	 * @return the acronimo
	 */
	public String getAcronimo() {
		return this.acronimo;
	}
	/**
	 * @param acronimo the acronimo to set
	 */
	public void setAcronimo(String acronimo) {
		this.acronimo = acronimo;
	}

}
