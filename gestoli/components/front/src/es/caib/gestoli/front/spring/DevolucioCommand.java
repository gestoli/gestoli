package es.caib.gestoli.front.spring;

import java.util.Date;

public class DevolucioCommand {

	private Long idSortida;
	private Long idLot;
	private Integer devolucioBotelles;
	private Integer totalBotelles;
	private Integer botellesTornades;
	private Date data;

	public Long getIdSortida() {
		return idSortida;
	}
	public void setIdSortida(Long idSortida) {
		this.idSortida = idSortida;
	}
	public Long getIdLot() {
		return idLot;
	}
	public void setIdLot(Long idLot) {
		this.idLot = idLot;
	}
	public Integer getDevolucioBotelles() {
		return devolucioBotelles;
	}
	public void setDevolucioBotelles(Integer devolucioBotelles) {
		this.devolucioBotelles = devolucioBotelles;
	}
	public Integer getTotalBotelles() {
		return totalBotelles;
	}
	public void setTotalBotelles(Integer totalBotelles) {
		this.totalBotelles = totalBotelles;
	}
	public Integer getBotellesTornades() {
		return botellesTornades;
	}
	public void setBotellesTornades(Integer botellesTornades) {
		this.botellesTornades = botellesTornades;
	}
	public Date getData() {
		return data;
	}
	public void setData(Date data) {
		this.data = data;
	}
}
