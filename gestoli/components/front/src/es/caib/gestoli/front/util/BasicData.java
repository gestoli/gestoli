package es.caib.gestoli.front.util;




public class BasicData  implements java.io.Serializable {


	private static final long serialVersionUID = 8825212191100027419L;

	private String id;

	private String nom;

	public BasicData(String id, String nom){
		this.id = id;
		this.nom = nom;
	}
	
	public BasicData(){}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	
}
