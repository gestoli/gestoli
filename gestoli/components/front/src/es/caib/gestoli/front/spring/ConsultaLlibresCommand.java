package es.caib.gestoli.front.spring;

import java.util.Date;


/**
 * Objeto que representa el formulario de consultas
 * @author Oriol Barnes (obarnes@at4.net)
 */
public class ConsultaLlibresCommand {
	private Date dataInici;
	private Date dataFi;
	private Integer tipusLlibre;
	private String mesura;

	public Date getDataFi() {
		return dataFi;
	}
	public void setDataFi(Date dataFi) {
		this.dataFi = dataFi;
	}
	public Date getDataInici() {
		return dataInici;
	}
	public void setDataInici(Date dataInici) {
		this.dataInici = dataInici;
	}
	public Integer getTipusLlibre() {
		return tipusLlibre;
	}
	public void setTipusLlibre(Integer tipusLlibre) {
		this.tipusLlibre = tipusLlibre;
	}
	public String getMesura() {
		return mesura;
	}
	public void setMesura(String mesura) {
		this.mesura = mesura;
	}



}
