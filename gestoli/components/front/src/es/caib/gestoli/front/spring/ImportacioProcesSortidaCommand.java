package es.caib.gestoli.front.spring;

import es.caib.gestoli.logic.model.Arxiu;


/**
 * Objeto que representa el formulario de mantenimento
 * de salida de lotes.
 * 
 * 
 */
public class ImportacioProcesSortidaCommand { 
	

	private byte[] fitxer;
	private Arxiu arxiuObject;
	private Long arxiu;
	
	private String tipusSortida;
	
	public ImportacioProcesSortidaCommand(){
		
	}

	
	public String getTipusSortida() {
		return tipusSortida;
	}


	public void setTipusSortida(String tipusSortida) {
		this.tipusSortida = tipusSortida;
	}


	public Long getArxiu() {
		return arxiu;
	}

	public void setArxiu(Long arxiu) {
		this.arxiu = arxiu;
	}

	/**
	 * @return the fitxer
	 */
	public byte[] getFitxer() {
		return fitxer;
	}

	/**
	 * @param fitxer the fitxer to set
	 */
	public void setFitxer(byte[] fitxer) {
		this.fitxer = fitxer;
	}

	/**
	 * @return the arxiuObject
	 */
	public Arxiu getArxiuObject() {
		return arxiuObject;
	}

	/**
	 * @param arxiuObject the arxiuObject to set
	 */
	public void setArxiuObject(Arxiu arxiuObject) {
		this.arxiuObject = arxiuObject;
	}
	
}
