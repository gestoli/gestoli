package es.caib.gestoli.front.spring;

import es.caib.gestoli.logic.model.SortidaOrujo;

/**
 * Objecte que representa el formulari de manteniment de sortides d'orujo.
 * 
 * @author Miquel Angel Amengual <miquelaa@limit.es>
 */
public class SortidaOrujoCommand extends SortidaOrujo {

	private static final long serialVersionUID = 1L;

	/**
	 * Omple els camps d'aquest objecte amb la informaci√≥ de la sortida d'orujo.
	 * 
	 * @param sortidaOrujo SortidaOrujo
	 */
	public void fromSortidaOrujo(SortidaOrujo sortidaOrujo) {
		setId(sortidaOrujo.getId());
		setData(sortidaOrujo.getData());
		setKilos(sortidaOrujo.getKilos());
		setAlbara(sortidaOrujo.getAlbara());
		setDesti(sortidaOrujo.getDesti());
		setDocument(sortidaOrujo.getDocument());
	}
}
